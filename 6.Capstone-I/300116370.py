# afficher la variable x de valeur 1 hour
x = '1 hour'
print(x)

# afficher la variable y de valeur 60 minutes
y = '60 minutes'
print(y)

# afficher l'equivalence de la valeur x et de la valeur y

x = True
y = False

if x or y :
    print('x is 1 hour or 60 minutes')

elif x or not y:
    print('x is 1 hour but not 60 minutes')    

else:
    print('x is not 1 hour or 60 minutes')
